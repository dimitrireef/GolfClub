package golf.club.app.model;

public class ListCircuit {

	String id="";
	String name="";
	String version="";

	public ListCircuit(String id, String name,String version){

		this.id=id;
		this.name=name;
		this.version=version;


	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getVersion() {
		return version;
	}




}
