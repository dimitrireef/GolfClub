package golf.club.app.model;

public class Circuit {

	String id="";
	String name="";
	String version="";

	public Circuit(String id1, String name1,String version1){

		this.id=id1;
		this.name=name1;
		this.version=version1;


	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getVersion() {
		return version;
	}




}
